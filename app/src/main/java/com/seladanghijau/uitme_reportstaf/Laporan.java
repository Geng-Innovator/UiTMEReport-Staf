package com.seladanghijau.uitme_reportstaf;

public class Laporan {
    private String id, laporan_imej, laporan_tempat, laporan_tarikh, laporan_masa, laporan_status;

    public Laporan() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLaporan_imej() {
        return laporan_imej;
    }

    public void setLaporan_imej(String laporan_imej) {
        this.laporan_imej = laporan_imej;
    }

    public String getLaporan_tempat() {
        return laporan_tempat;
    }

    public void setLaporan_tempat(String laporan_tempat) {
        this.laporan_tempat = laporan_tempat;
    }

    public String getLaporan_tarikh() {
        return laporan_tarikh;
    }

    public void setLaporan_tarikh(String laporan_tarikh) {
        this.laporan_tarikh = laporan_tarikh;
    }

    public String getLaporan_masa() {
        return laporan_masa;
    }

    public void setLaporan_masa(String laporan_masa) {
        this.laporan_masa = laporan_masa;
    }

    public String getLaporan_status() {
        return laporan_status;
    }

    public void setLaporan_status(String laporan_status) {
        this.laporan_status = laporan_status;
    }
}
